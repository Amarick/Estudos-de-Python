# Escreva um programa que converta uma temperatura digitada em °C em °F. A fórmula para essa conversão é: F = (9 * C / 5) + 32

temperaturaCelsius =  float(input("Digite uma temperatura em °C: "))
Fahrenhreit = (9 * temperaturaCelsius / 5) + 32

print(f"A temperatura {temperaturaCelsius}°C é igual a {Fahrenhreit}°F" )